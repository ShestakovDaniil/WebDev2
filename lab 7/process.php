<?php
// Проверяем, что запрос пришел методом POST
if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    echo '<h2>Доступ запрещен. Используйте форму ввода.</h2>';
    exit();
}

// 1. Проверка наличия входных данных
// Если массив не был передан, выводим сообщение и прерываем выполнение
if (!isset($_POST['element0']) || $_POST['element0'] === '') {
    echo '<h2>Массив не задан, сортировка невозможна</h2>';
    exit(); // Досрочное прекращение выполнения PHP-программы
}

// Определение длины массива из скрытого поля
$arrLength = (int)($_POST['arrLength'] ?? 0);
$inputArray = [];
$isValid = true;

/**
 * Функция проверки: возвращает true, если аргумент НЕ является числом.
 * Адаптировано по Листингу А-7.4 (исправлена логическая ошибка оригинала, 
 * где проверка if($arg!=='') возвращала true для любой непустой строки).
 */
function arg_is_not_Num($arg) {
    if ($arg === '') return true; // Пустая строка не является числом
    
    $len = strlen($arg);
    // Цикл перебирает каждый символ строки
    for ($i = 0; $i < $len; $i++) {
        $char = $arg[$i];
        // Разрешаем цифры, минус и точку (для дробных чисел)
        if ($char !== '-' && $char !== '.' && ($char < '0' || $char > '9')) {
            return true; // Встретился недопустимый символ -> не число
        }
    }
    return false; // Все символы допустимы -> это число
}

// 2. Валидация входных данных и формирование массива
// Цикл проходит по всем полям формы согласно переданной длине
for ($i = 0; $i < $arrLength; $i++) {
    $value = $_POST["element{$i}"] ?? '';
    
    // Проверяем текущий элемент на числовое значение
    if (arg_is_not_Num($value)) {
        echo '<p style="color: red; font-weight: bold;">Ошибка валидации: Элемент массива "' . htmlspecialchars($value) . '" – не число. Сортировка не выполняется.</p>';
        $isValid = false;
        break; // Прерываем цикл при первой же ошибке
    }
    $inputArray[] = (float)$value; // Приводим строку к числу и добавляем в массив
}

// Если валидация успешна, приступаем к сортировке
if ($isValid) {
    // Получаем выбранный алгоритм из селектора
    $algorithm = $_POST['algorithm'] ?? 'php';
    $algoNames = [
        'selection' => 'Сортировка выбором',
        'bubble'    => 'Пузырьковый алгоритм',
        'shell'     => 'Алгоритм Шелла',
        'gnome'     => 'Алгоритм садового гнома',
        'quick'     => 'Быстрая сортировка',
        'php'       => 'Встроенная функция PHP для сортировки списков по значению'
    ];
    $algoTitle = $algoNames[$algorithm] ?? 'Неизвестный алгоритм';

    // Вывод заголовка, входных данных и сообщения о валидации
    echo "<h1>{$algoTitle}</h1>";
    echo "<h3>Входные данные:</h3>";
    echo '<div class="arr_element">' . implode(', ', $inputArray) . '</div>';
    echo '<p style="color: green;">Массив проверен, все элементы являются числами. Сортировка возможна.</p>';

    // Засекаем время начала сортировки с точностью до микросекунды
    $timeStart = microtime(true);
    $iterations = 0;

    // Вызов соответствующей функции сортировки через switch
    switch ($algorithm) {
        case 'selection': $iterations = sort_selection($inputArray); break;
        case 'bubble':    $iterations = sort_bubble($inputArray); break;
        case 'shell':     $iterations = sort_shell($inputArray); break;
        case 'gnome':     $iterations = sort_gnome($inputArray); break;
        case 'quick':     sort_quick($inputArray, 0, count($inputArray) - 1, $iterations); break;
        case 'php':       sort_php($inputArray, $iterations); break;
    }

    // Засекаем время окончания и вычисляем разницу
    $timeEnd = microtime(true);
    $timeDiff = $timeEnd - $timeStart;

    // Вывод результата и итоговой статистики
    echo "<h3>Результат сортировки:</h3>";
    echo '<div class="arr_element">' . implode(', ', $inputArray) . '</div>';
    echo "<p>Сортировка завершена, проведено {$iterations} итераций. Сортировка заняла " . number_format($timeDiff, 6) . " секунд.</p>";
}
exit(); // Завершаем работу после вывода результатов

// Листинг А-7.4 (адаптирован): Сортировка выбором
function sort_selection(&$arr) {
    $iter = 0;
    $n = count($arr);
    
    // Цикл проходит по всем элементам массива, кроме последнего
    for ($i = 0; $i < $n - 1; $i++) {
        $minIdx = $i; // Считаем текущий элемент минимальным
        
        // Внутренний цикл ищет минимальный элемент в неотсортированной части
        for ($j = $i + 1; $j < $n; $j++) {
            $iter++; // Увеличиваем счетчик итераций на каждом шаге сравнения
            if ($arr[$j] < $arr[$minIdx]) {
                $minIdx = $j; // Запоминаем индекс найденного минимума
            }
            // Вывод состояния массива на каждой итерации (сквозная нумерация)
            echo "Итерация {$iter}: [" . implode(', ', $arr) . "]<br>";
        }
        
        // Если найденный минимум не на текущей позиции, меняем элементы местами
        if ($minIdx != $i) {
            $temp = $arr[$i];
            $arr[$i] = $arr[$minIdx];
            $arr[$minIdx] = $temp;
        }
    }
    return $iter; // Возвращаем общее количество итераций
}

// Листинг А-7.7 (адаптирован): Пузырьковый алгоритм
function sort_bubble(&$arr) {
    $iter = 0;
    $n = count($arr);
    
    // Внешний цикл: количество проходов равно длине массива минус 1
    for ($j = 0; $j < $n - 1; $j++) {
        // Внутренний цикл: сравниваем соседние элементы, не затрагивая уже отсортированный "хвост"
        for ($i = 0; $i < $n - 1 - $j; $i++) {
            $iter++; // Считаем каждую операцию сравнения/прохода
            // Если текущий элемент больше следующего, меняем их местами (по возрастанию)
            if ($arr[$i] > $arr[$i + 1]) {
                $temp = $arr[$i];
                $arr[$i] = $arr[$i + 1];
                $arr[$i + 1] = $temp;
            }
            // Вывод состояния массива после каждой итерации сравнения/перестановки
            echo "Итерация {$iter}: [" . implode(', ', $arr) . "]<br>";
        }
    }
    return $iter;
}

// Листинг А-7.12 (адаптирован): Алгоритм Шелла
function sort_shell(&$arr) {
    $iter = 0;
    $n = count($arr);
    
    // Цикл вычисляет последовательность шагов (расстояний): N/2, N/4, ..., 1
    for ($gap = floor($n / 2); $gap > 0; $gap = floor($gap / 2)) {
        // Цикл проходит по элементам, начиная с индекса шага
        for ($i = $gap; $i < $n; $i++) {
            $temp = $arr[$i]; // Сохраняем текущий элемент
            $j = $i;
            
            // Цикл с предусловием сдвигает элементы на расстояние gap вправо
            while ($j >= $gap && $arr[$j - $gap] > $temp) {
                $iter++;
                $arr[$j] = $arr[$j - $gap]; // Сдвигаем элемент
                $j -= $gap;
                echo "Итерация {$iter}: [" . implode(', ', $arr) . "]<br>";
            }
            $arr[$j] = $temp; // Вставляем сохраненный элемент на найденное место
        }
    }
    return $iter;
}

// Листинг А-7.10/11 (адаптирован): Алгоритм садового гнома
function sort_gnome(&$arr) {
    $iter = 0;
    $i = 1; // Начинаем со второго элемента (индекс 1)
    $n = count($arr);
    
    // Цикл выполняется, пока гном не дойдет до конца массива
    while ($i < $n) {
        $iter++;
        // Если гном в начале или текущий элемент больше/равен предыдущему -> шагаем вперед
        if ($i == 0 || $arr[$i - 1] <= $arr[$i]) {
            $i++;
        } else {
            // Иначе меняем местами с предыдущим и шагаем назад
            $temp = $arr[$i];
            $arr[$i] = $arr[$i - 1];
            $arr[$i - 1] = $temp;
            $i--;
        }
        echo "Итерация {$iter}: [" . implode(', ', $arr) . "]<br>";
    }
    return $iter;
}

// Листинг А-7.13 (адаптирован): Быстрая сортировка
function sort_quick(&$arr, $left, $right, &$iter) {
    if ($left >= $right) return; // Базовый случай рекурсии: часть массива пуста или содержит 1 элемент
    
    $l = $left;
    $r = $right;
    // Выбираем опорную точку (центральный элемент)
    $pivot = $arr[floor(($left + $right) / 2)];
    
    do {
        $iter++;
        // Двигаем левую границу вправо, пока элемент меньше опорного
        while ($arr[$l] < $pivot) $l++;
        // Двигаем правую границу влево, пока элемент больше опорного
        while ($arr[$r] > $pivot) $r--;
        
        if ($l <= $r) {
            // Меняем элементы местами
            $temp = $arr[$l];
            $arr[$l] = $arr[$r];
            $arr[$r] = $temp;
            $l++;
            $r--;
        }
        echo "Итерация {$iter}: [" . implode(', ', $arr) . "]<br>";
    } while ($l <= $r);
    
    // Рекурсивный вызов для левой части
    if ($left < $r) sort_quick($arr, $left, $r, $iter);
    // Рекурсивный вызов для правой части
    if ($l < $right) sort_quick($arr, $l, $right, $iter);
}

// Встроенная функция PHP
function sort_php(&$arr, &$iter) {
    // sort() сортирует массив по возрастанию с сохранением числовых ключейF
    sort($arr, SORT_NUMERIC);
    // Встроенная функция C-уровня не позволяет отследить итерации интерпретатора
    $iter = 0; 
    echo "Встроенная сортировка PHP выполнена на уровне интерпретатора.<br>";
}
